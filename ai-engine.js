    if(!window.IRAQI_LAWS || !Array.isArray(window.IRAQI_LAWS)) return null;
    
    const q = query.toLowerCase();
    const words = q.split(/\s+/).filter(w => w.length > 2);
    const results = [];
    
    for(const law of window.IRAQI_LAWS) {
      let score = 0;
      const titleLower = law.title.toLowerCase();
      const titleEnLower = (law.titleEn || '').toLowerCase();
      
      // Title match
      for(const word of words) {
        if(titleLower.includes(word)) score += 3;
        if(titleEnLower.includes(word)) score += 2;
      }
      
      // Article content match
      for(const art of law.articles) {
        const artLower = art.text.toLowerCase();
        for(const word of words) {
          if(word.length > 3 && artLower.includes(word)) score += 1;
        }
      }
      
      if(score > 0) {
        results.push({law: law, score: score});
      }
    }
    
    results.sort((a, b) => b.score - a.score);
    return results.slice(0, 10);
